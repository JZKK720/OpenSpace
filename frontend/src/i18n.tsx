function interpolate(template: string, params?: MessageParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key];
    return value === undefined || value === null ? '' : String(value);
  });
}

function resolveMessage(language: Language, key: string, params?: MessageParams): string {
  const entry = translations[language][key] ?? translations.en[key] ?? key;
  if (typeof entry === 'function') {
    return entry(params);
  }
  return interpolate(entry, params);
}

function getInitialLanguage(): Language {
  if (typeof window === 'undefined') {
    return 'en';
  }

  const stored = window.localStorage.getItem(STORAGE_KEY);
  if (stored === 'en' || stored === 'zh-CN') {
    return stored;
  }

  return window.navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en';
}

interface I18nContextValue {
  language: Language;
  isChinese: boolean;
  setLanguage: (language: Language) => void;
  t: (key: string, params?: MessageParams) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function getDocumentLocale(): string {
  if (typeof document === 'undefined') {
    return 'en-US';
  }

  return document.documentElement.lang.startsWith('zh') ? 'zh-CN' : 'en-US';
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => getInitialLanguage());

  useEffect(() => {
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(STORAGE_KEY, language);
    }
    if (typeof document !== 'undefined') {
      document.documentElement.lang = language;
      document.title = language === 'zh-CN' ? 'Cubecloud 仪表盘' : 'Cubecloud Dashboard';
    }
  }, [language]);

  const value = useMemo<I18nContextValue>(() => ({
    language,
    isChinese: language === 'zh-CN',
    setLanguage,
    t: (key: string, params?: MessageParams) => resolveMessage(language, key, params),
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}